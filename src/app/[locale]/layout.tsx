import "@/shared/assets/css/globals.css";
import ReactQueryProvider from "@/shared/lib/ReactQueryProvider";
import SessionProviderAuth from "@/shared/lib/SessionProvider";
import { LocaleType } from "@/shared/types/locale";
import type { Metadata } from "next";
import { getServerSession } from "next-auth";
import { NextIntlClientProvider } from "next-intl";
import { getMessages, unstable_setRequestLocale } from "next-intl/server";
import Bar from "./(digital_sales)/_components/Bar";
import FooterSales from "./(digital_sales)/_components/FooterSales";

export const metadata: Metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
  manifest: "/manifest.json",
  icons: "https://simaat.app/wp-content/uploads/2022/11/favicon-32x32-1.png",
};

export type RootLayoutProps = {
  params: { locale: LocaleType };
};

export default async function RootLayout({
  children,
  params: { locale },
}: Readonly<{
  children: React.ReactNode;
  params: { locale: LocaleType };
}>) {
  const session = await getServerSession();
  const messages = await getMessages();
  unstable_setRequestLocale(locale);

  return (
    <html lang={locale} dir={locale === "en" ? "ltr" : "rtl"}>
      <body suppressHydrationWarning={true}>
        <NextIntlClientProvider messages={messages}>
          <ReactQueryProvider>
            <SessionProviderAuth session={session}>
              <Bar />
              {children}
              <FooterSales />
            </SessionProviderAuth>
          </ReactQueryProvider>
        </NextIntlClientProvider>
      </body>
    </html>
  );
}
